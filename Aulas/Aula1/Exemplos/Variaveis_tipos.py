a = 42
print('Variavel a:', a)
print('Tipo da variavel a:', type(a))
print('')

# Vou transformar "num" em float
a_tipo_float = float(a)
print('Variavel a tipo float:', a_tipo_float)
print('Tipo da variavel a:', type(a_tipo_float))
print('')

# Vou transformar "num" em string
a_tipo_str = str(a)
print('Variavel a tipo str:', a_tipo_str)
print('Tipo da variavel a:', type(a_tipo_str))
print('')

# Vou transformar num em boolean
a_tipo_bool = bool(a)
print('Variavel a tipo bool:', a_tipo_bool)
print('Tipo da variavel a:', type(a_tipo_bool))
print('')
